WITH cast_date AS
(
SELECT 
	TIMESTAMP
	, CAST(TIMESTAMP AS DATE) AS LOAD_DATE
	, CAST(TIMESTAMP AS TIME) AS LOAD_TIME
	, GPSNORTHING
	, GPSEASTING
	, GPSELEVATION
	, FUEL_RATE
	, STATUS
	, PAYLOAD
	, TRUCK_ID
	, TRUCK_TYPE_ID
	, SHOVEL_ID
	, DUMP_ID
	, RND
  FROM [Teck].[dbo].[df]
)
SELECT * FROM cast_date
WHERE LOAD_DATE = '2022-04-08' 
--AND TRUCK_ID = 3
AND STATUS = 'Hauling'
ORDER BY LOAD_TIME